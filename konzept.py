## "SafeMyPass" - Password-Management System
#[GitHub](https://github.com/anastasiarodionova/djangogirls_566061.git)

# SafeMyPass ist ein webbasiertes Password-Management System.
# Django bietet ein flexibles Passwort-Speichersystem und verwendet standardmäßig PBKDF2.


## Aufgabenstellung:
# 1. User könnte sich auf das System mit einem Master-Password einloggen.
# 2. Master-Key wird mit einem einzigartigen Salz gehashed.
# 4. Datenbank soll erstellt werden.
# 4. Innerhalb der Datenbank werden jedes Passwort und jede Notiz mit einem eindeutigen Salz verschlüsselt Mithilfe von pycryptodome.
# 5. Passwörter werden in einer verschlüsselten SQLite-Datenbank gespeichert.
# 6. Passwort-Vorschläge mit password-generator-py (English)
# 7. Mit Hilfe von Unittest testen

## Ziel:
# Alle Informationen und Passwords sollen sicher generiert/hashed und gespeichert werden


## Technologien:
